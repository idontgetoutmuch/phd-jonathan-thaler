-module(merge).

%% ====================================================================
%% API functions
%% ====================================================================
-export([merge/2]).

merge( [], L ) -> 
	L;
merge( [ H | T1 ], L2 ) -> 
	[ H | merge( T1, L2 ) ];
merge( L1, L2 ) -> 
	merge(L1, L2).

