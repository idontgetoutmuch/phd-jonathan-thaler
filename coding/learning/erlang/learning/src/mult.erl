-module(mult).

%% ====================================================================
%% API functions
%% ====================================================================
-export([mult/2]).


mult( X, Y ) -> 
	multRec( X, Y, 0 ).

%% ====================================================================
%% Internal functions
%% ====================================================================

multRec( 0, Y, ACC ) -> 
		ACC;
multRec( X, Y, ACC ) ->
		multRec( X - 1, Y, ACC + Y ).
